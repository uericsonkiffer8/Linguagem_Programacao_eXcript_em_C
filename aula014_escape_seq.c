int main ()
{
    /*
    1) \n -> quebramos uma linha;
    2) \t -> dar um tab no momento em que estamos imprimindo;
    3) \\ -> imprimir uma barra invertida;
    4) \' -> para imprimir aspas simples;
    5) \" -> para imprimir aspas duplas;
    6) \0 -> aqui finaliza o nosso texto;  
    
    
    */
    printf("\t============================\n\n");
    printf("\t\tEu sou o \n \t>>>Code::Block<<<\n\n");
    printf("\t============================\n\n");
    printf("\tPara imprimir aspas simples:\t \'\n\n");
    printf("\tPara imprimir aspas duplas:\t \"\n\n");
    printf("\tPara imprimir barra invertida:\t \\\n\n");
    return 0;
}